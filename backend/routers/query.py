@router.post("/multi")
async def query_multi(req: QueryRequest):
   """
   Ask a question across multiple documents. The request can include either a list of doc_ids or no doc_id at all (which means search across all docs).
    
    
   """
    
   doc_ids = getattr(req, 'doc_ids', None) or []
   if not doc_ids:
     raise HTTPException(
       status_code=400,
       detail="doc_ids list is required for multi-document queries"
     )
   query_vector = embed_query(req.question)
   raw_chunks = search_chunks(
     query_vector=query_vector,
     doc_ids=doc_ids,
   )
    
   if not raw_chunks:
     raise HTTPException(status_code=404, detail="No relevant content found across the specified documents.")
    
   answer = answer_question(
      question=req.question,
      chunks=raw_chunks,
      category='general',
      conversation_history=req.conversation_history or [],
    )
    
    # group souurces by document so flutter can show
    # which chunks came from which document
    
   sources_by_doc = {}
   for chunk in raw_chunks:
      fname = chunk['filename']
      if fname not in sources_by_doc:
        sources_by_doc[fname] = []
      sources_by_doc[fname].append(chunk)
      
   return {
     'answer': answer,
     'sources': _format_sources(raw_chunks),
     'sources_by_doc': sources_by_doc,
     'doc_count': len(sources_by_doc),
     'total_chunks': len(raw_chunks),
     'model_used': 'gemini-2.5-flash',
   }