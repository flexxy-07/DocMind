@router.post("/stream")
async def query_stream(req: QueryRequest):
  raw_chunks, category, is_image_doc, _ = await _retrieve(req)

  if is_image_doc:
    raise HTTPException(
      status_code=400,
      detail="Streaming is not supported for image-only documents. Use /query instead."
    )
  
  sources_payload = [
    {
      'text' : chunk['text'][:300] + '...' if len(chunk['text']) > 300 else chunk['text'],
      'page': chunk.get('page', None),
      'score': chunk['score'],
      'doc_id': chunk['doc_id'],
      'filename': chunk['filename'],
    }
    for chunk in raw_chunks
  ]
  async def answer_generator():
    try:
      async for token in stream_answer(
        question=req.question,
        chunks=raw_chunks,
        category=category,
        conversation_history=req.conversation_history or [],
      ):
        # SSE format: "data: {token}\n\n
        # The double newline indicates the end of one message
        event = json.dumps({
          'type': 'token',
          'content': token,
        })
        yield f"data: {event}\n\n"
      
      event = json.dumps({
        'type': 'end',
        'content': sources_payload
      })
      yield f"data: {event}\n\n"
      
      event = json.dumps({
        'type' : 'metadata',
        'category' : category,
      })
      yield f"data: {event}\n\n"
    except Exception as e:
      # if anything goes wrong, send an error event
      event = json.dumps({
        'type': 'error',
        'content': str(e),
      })
      yield f"data: {event}\n\n"
      
  return StreamingResponse(answer_generator(), media_type="text/event-stream", headers={
    # tell proxies and CDNs not to cache this response
    "Cache-Control": "no-cache",
    "X-Accel-Buffering": "no",  # for nginx: disable 
  })
  
  
